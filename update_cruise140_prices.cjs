const prices = {};
priceData.split('\n').forEach(line => {
  const [id, base, finished] = line.trim().split('\t');
  const baseNum = parseInt(base);
  const finishedNum = finished === 'null' ? null : parseInt(finished);
  if (id && !isNaN(baseNum)) {
    prices[id] = { base: baseNum, finished: finishedNum };
  }
});

console.log(`Loaded ${Object.keys(prices).length} price entries`);

// Find the Cruise - 140 building
const buildingIndex = buildings.findIndex(b => b.name === 'Cruise - 140');
if (buildingIndex === -1) {
  console.error('Building "Cruise - 140" not found!');
  console.log('Available buildings:', buildings.map(b => b.name).join(', '));
  process.exit(1);
}

const building = buildings[buildingIndex];
console.log(`Found building: ${building.name} with ${building.units.length} units`);

let updated = 0;
let skippedActive = 0;
let notFound = 0;
const skippedList = [];
const updatedList = [];

building.units.forEach(unit => {
  // Match by unitId field (e.g. "CA103")
  const newPrice = prices[unit.unitId];
  if (!newPrice) return;

  // Check for active offers/contracts
  if (activeOfferUnits.has(unit.unitId)) {
    skippedActive++;
    skippedList.push(`${unit.unitId} (active offer)`);
    return;
  }
  if (activeContractUnits.has(unit.unitId)) {
    skippedActive++;
    skippedList.push(`${unit.unitId} (active contract)`);
    return;
  }

  const oldBase = unit.price;
  const oldFinished = unit.finishedPrice;

  // Update base price
  unit.price = newPrice.base;
  
  // Update finished price (if provided, skip #REF! entries)
  if (newPrice.finished !== null) {
    unit.finishedPrice = newPrice.finished;
  }

  updated++;
  updatedList.push(`${unit.unitId}: base ${oldBase} -> ${newPrice.base}` +
    (newPrice.finished !== null ? `, finished ${oldFinished} -> ${newPrice.finished}` : ` (finished kept: ${oldFinished})`));
  
  // Mark as processed
  delete prices[unit.unitId];
});

// Check which price entries didn't match any unit
const remaining = Object.keys(prices);
console.log(`\n=== RESULTS ===`);
console.log(`Updated: ${updated}`);
console.log(`Skipped (active offer/contract): ${skippedActive}`);
if (skippedList.length > 0) {
  skippedList.forEach(s => console.log(`  ⛔ ${s}`));
}
if (remaining.length > 0) {
  console.log(`Not found in building: ${remaining.length}`);
  remaining.forEach(id => console.log(`  ⚠️ ${id}`));
}

