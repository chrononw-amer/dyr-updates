/**
 * Get translation for a key
 * @param {string} key - Translation key (e.g., 'home.buildings')
 * @param {object} params - Optional parameters for interpolation
 * @returns {string} Translated string or key if not found
 */
export const t = (key, params = {}) => {
    const dict = translations[currentLanguage] || translations.en;
    let text = dict[key] || translations.en[key] || key;

    // Simple parameter interpolation: {{param}}
    Object.keys(params).forEach(param => {
        text = text.replace(new RegExp(`{{${param}}}`, 'g'), params[param]);
    });

    return text;
};

/**
 * Set the current language
 * @param {string} lang - Language code ('en', 'ar', etc.)
 */
export const setLanguage = (lang) => {
    if (translations[lang]) {
        currentLanguage = lang;
        localStorage.setItem('app_language', lang);

        // Update document direction for RTL languages
        document.documentElement.dir = isRTL() ? 'rtl' : 'ltr';
        document.documentElement.lang = lang;

        // Dispatch event for React components to re-render
        window.dispatchEvent(new CustomEvent('languageChange', { detail: { language: lang } }));
    }
};

/**
 * Get current language code
 * @returns {string} Current language code
 */
export const getCurrentLanguage = () => currentLanguage;

/**
 * Check if current language is RTL
 * @returns {boolean} True if RTL language
 */
export const isRTL = () => {
    const rtlLanguages = ['ar', 'he', 'fa', 'ur'];
    return rtlLanguages.includes(currentLanguage);
};

/**
 * Get all available languages
 * @returns {Array} Array of {code, name} objects
 */
export const getAvailableLanguages = () => [
    { code: 'en', name: 'English', nativeName: 'English' },
    { code: 'ar', name: 'Arabic', nativeName: 'العربية' }
];

// Initialize direction on load
if (typeof document !== 'undefined') {
    document.documentElement.dir = isRTL() ? 'rtl' : 'ltr';
    document.documentElement.lang = currentLanguage;
}

export default { t, setLanguage, getCurrentLanguage, isRTL, getAvailableLanguages };
